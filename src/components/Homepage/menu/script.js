
 
   export function openNav(){
    document.getElementById('Hpmenu').style.display="block"
    document.getElementById('HpbuttonMenu').style.display="none"
    document.getElementById('HpbuttonClose').style.display = "block"
   }
   export function closeNav(){
    document.getElementById('Hpmenu').style.display="none"
    document.getElementById('HpbuttonMenu').style.display="block"
   }

